function loadTemplate(templateUrl) {
  return fetch(templateUrl)
    .then(response => response.text());
}

function insertTemplate(template, data, containerId) {
  console.log("hello world");
  const container = document.getElementById(containerId);
  const renderedTemplate = template.replace(/{{(\w+)}}/g, (_, key) => data[key] || '');
  container.innerHTML += renderedTemplate;
}

function populateSidebar1() {
  loadTemplate('../html/templates/side-bar-item.html').then(template => {
    sidebarItems1.forEach(item => {
      insertTemplate(template, item, 'side-row1');
    });
  });
}


